    /**
     * Handles the HTTP
     * <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("/WEB-INF/view/purchaseCustomize.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP
     * <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        Customize custom = (Customize) session.getAttribute("customize");
        boolean success = false;
        if (custom != null) {
            UserOrder order = formPurchase.createMount(request);
            request.setAttribute("order", order);
            success = (order == null ? false : true);
            request.setAttribute("form", formPurchase);
            if(success)
                session.removeAttribute("customize");
        }
        request.setAttribute("success", success);
        
        request.getRequestDispatcher("/WEB-INF/view/purchaseDone.jsp").forward(request, response);
    }

